    private void jButton3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton3MouseClicked
        try {
            ap = new administrador_binario("./Users.opa");
            ap.cargarArchivo();
            int aux = 0;
            String nombre = tf_nombre.getText();
            int edad = (int) sp_edad.getValue();
            String sexo = wombo_sexo.getSelectedItem().toString();
            String user = tf_user.getText();
            String contra = tf_contra.getText();
            String descripcion = ta_descripcion.getText();
            if (tf_nombre.getText().equals("") || tf_user.getText().equals("") || tf_contra.getText().equals("") || ta_descripcion.getText().equals("")) {
                aux = 5;
            }
            String premuim_s_n = wombo_premium.getSelectedItem().toString();
            for (Usuario u : lista) {
                if (tf_user.getText().equals(u.getUser())) {
                    aux = 1;
                }
            }
            if (contra.length() <= 7) {
                aux = 2;

            }
            if (aux == 0) {
                ArrayList<Intereses> ints = new ArrayList();
                String[] op = tf_intereses.getText().split(",");
                for (int i = 0; i < op.length; i++) {
                    ints.add(new Intereses(op[i]));
                }
                lista.add(new Usuario(nombre, edad, sexo, user, contra, descripcion, icon, premuim_s_n));
                Usuario u = new Usuario(nombre, edad, sexo, user, contra, descripcion, icon, premuim_s_n);
                u.setIntereses(ints);
                u.setUsuarios_NO_bloqueados(ap.getListausuarios());
                ap.getListausuarios().add(u);
                ap.escribir();
                llenar_kja();
                //llenar_kja2();
//                llenar_lista();
                JOptionPane.showMessageDialog(this, "Amigo guardado exitosamente");
                tf_nombre.setText("");
                sp_edad.setValue(18);
                tf_user.setText("");
                tf_contra.setText("");
                ta_descripcion.setText("");
            }
            if (aux == 2) {
                JOptionPane.showMessageDialog(this, "La contrasena debe tener al menos 8 caracteres");
            } else if (aux == 1) {
                JOptionPane.showMessageDialog(this, "El usuario ya esta en Uso");
            } else if (aux == 5) {
                JOptionPane.showMessageDialog(this, "Debe rellenar todos los campos");
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton3MouseClicked

